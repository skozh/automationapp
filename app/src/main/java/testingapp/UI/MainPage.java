    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().chars().allMatch(Character::isWhitespace) 
                || jTextField2.getText().chars().allMatch(Character::isWhitespace)
                  || jTextField3.getText().chars().allMatch(Character::isWhitespace)
                    || jTextField4.getText().chars().allMatch(Character::isWhitespace)){
            JOptionPane.showMessageDialog(this, "Enter all the fields");
        }
        else{
            String inputpath = jTextField1.getText().trim();
            String outputpath = jTextField2.getText().trim();
            String dbname = jTextField4.getText().trim();
            
            String dbpath = new File(outputpath, dbname).getAbsolutePath();
            File inputdir = new File(inputpath);
            if (jRadioButton1.isSelected()){
                File[] files = inputdir.listFiles((d, name) -> name.endsWith(".xlsx"));
                if (files.length==0){
                    JOptionPane.showMessageDialog(this, "No Excel input files found");
                }
                else{
                    system_busy();
                    DbLoader.load_excel(files, dbpath);
                    system_available();
                }
            }
            else{
                char delimiter = DEFAULT_DELIMITER;
                if (!jTextField3.getText().trim().chars().allMatch(Character::isWhitespace)){
                    delimiter = jTextField3.getText().trim().charAt(0);
                }
                File[] files = inputdir.listFiles((d, name) -> name.endsWith(".csv"));
                if (files.length==0){
                    JOptionPane.showMessageDialog(this, "No CSV input files found");
                }
                else{
                    system_busy();
                    DbLoader.load_csv(files, dbpath, delimiter);
                    system_available();
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

